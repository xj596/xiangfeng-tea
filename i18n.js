/* ============================================================
   i18n API
   ============================================================ */
var XF_i18n = {
  current: 'en',

  init: function() {
    var saved = localStorage.getItem('xf_lang');
    if (saved && XF_I18N_TRANSLATIONS[saved]) {
      this.current = saved;
    } else {
      var browser = (navigator.language || '').split('-')[0];
      if (browser === 'zh') this.current = 'zh';
      else if (browser === 'ru') this.current = 'ru';
      else if (browser === 'ar') this.current = 'ar';
      else if (browser === 'fr') this.current = 'fr';
      else this.current = 'en';
    }
    this.applyLang();
    this.injectLangSelector();
  },

  setLang: function(lang) {
    if (!XF_I18N_TRANSLATIONS[lang]) return;
    this.current = lang;
    localStorage.setItem('xf_lang', lang);
    this.applyLang();
    document.dispatchEvent(new CustomEvent('xf_lang_change', {detail:{lang:lang}}));
  },

  applyLang: function() {
    document.documentElement.lang = this.current;
    document.documentElement.dir = (this.current === 'ar') ? 'rtl' : 'ltr';
    this.updatePageText();
  },

  t: function(key) {
    var keys = key.split('.');
    var val = XF_I18N_TRANSLATIONS[this.current];
    for (var i = 0; i < keys.length; i++) {
      if (!val) return key;
      val = val[keys[i]];
    }
    return val || key;
  },

  updatePageText: function() {
    this.updateDataI18n('data-i18n');
    this.updateDataI18nPh('data-i18n-ph');
    this.updateLangSelectorUI();
  },

  updateDataI18n: function(attr) {
    var elements = document.querySelectorAll('[' + attr + ']');
    for (var i = 0; i < elements.length; i++) {
      var el = elements[i];
      var key = el.getAttribute(attr);
      if (key) el.textContent = this.t(key);
    }
  },

  updateDataI18nPh: function(attr) {
    var elements = document.querySelectorAll('[' + attr + ']');
    for (var i = 0; i < elements.length; i++) {
      var el = elements[i];
      var key = el.getAttribute(attr);
      if (key) el.placeholder = this.t(key);
    }
  },

  injectLangSelector: function() {
    /* Selectors are now pre-rendered in HTML — no DOM injection needed.
       Just bind the outside-click handler to close open dropdowns. */
    document.addEventListener('click', function(e) {
      var selectors = document.querySelectorAll('.lang-selector.open');
      for (var i = 0; i < selectors.length; i++) {
        if (!selectors[i].contains(e.target)) {
          selectors[i].classList.remove('open');
        }
      }
    });
  },

  updateLangSelectorUI: function() {
    var flags = {en:'🇬🇧', ru:'🇷🇺', ar:'🇸🇦', fr:'🇫🇷', zh:'🇨🇳'};
    var names = {en:'English', ru:'Русский', ar:'العربية', fr:'Français', zh:'中文'};
    var currents = document.querySelectorAll('.lang-selector .lang-current');
    for (var i = 0; i < currents.length; i++) {
      var cur = currents[i];
      var flagEl = cur.querySelector('.lang-flag');
      var labelEl = cur.querySelector('.lang-label');
      if (flagEl) flagEl.textContent = flags[this.current];
      if (labelEl) labelEl.textContent = names[this.current];
    }
    var options = document.querySelectorAll('.lang-selector .lang-option');
    for (var j = 0; j < options.length; j++) {
      var opt = options[j];
      if (opt.getAttribute('data-lang') === this.current) {
        opt.classList.add('active');
      } else {
        opt.classList.remove('active');
      }
    }
  }
};

/* Shortcut */
function __(key) { return XF_i18n.t(key); }

/* Initialize on DOM ready */
document.addEventListener('DOMContentLoaded', function() {
  XF_i18n.init();
});
